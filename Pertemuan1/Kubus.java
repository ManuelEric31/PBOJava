public class Kubus {
    public static void main(String[] args) {
        float rusuk = 12.5f;
        float luas = 6 * (rusuk * rusuk);
        float keliling = 12 * rusuk;

        System.out.println("Rusuk   : " + rusuk);
        System.out.println("Luas    : " + luas);
        System.out.println("Keliling: " + keliling);
    }
}
